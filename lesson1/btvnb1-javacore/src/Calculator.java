public class Calculator {

    public static int sum (int a, int b){
        return a+b;

    }
    public static int hieu (int a, int b){
        return a-b;

    }
    public static int chia (int a, int b){
        return a/b;
    }

    public static int nhan (int a, int b){
        return a*b;
    }
}
